    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (JtextContraseña.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(null, "Porfavor indique una Contraseña", "Error falta Contraseña", JOptionPane.ERROR_MESSAGE);
        }
        else{
            usuario = datosUsuario.buscarUsername(JtextUsuario.getText());//busca username en el xml
            if(usuario != null){//si encuentra el username
                System.out.println(usuario.getPassword());
                if(usuario.getPassword().equals(JtextContraseña.getText())){//si lo que escribio de passwird coincide con el xml

                    
                    
                    if("Administrador".equals(usuario.getRol())){

                        java.awt.EventQueue.invokeLater(new Runnable() {

                            public void run() {
                                new PantallaInicialAdminUsuario(usuario).setVisible(true);
                            }
                        });
                        this.setVisible(false);

                    }

                    if("Inventario".equals(usuario.getRol())){

                        java.awt.EventQueue.invokeLater(new Runnable() {

                            public void run() {
                                new PantallaInicialInvUsuario(usuario).setVisible(true);
                            }
                        });
                        this.setVisible(false);

                        System.out.println(usuario.getRol());

                    }
                    if("Comprador".equals(usuario.getRol())){

                        java.awt.EventQueue.invokeLater(new Runnable() {

                            public void run() {
                                new PantallaInicialCompradorUsuario(usuario).setVisible(true);
                            }
                        });
                        this.setVisible(false);

                    }

                }
                else{
                    JOptionPane.showMessageDialog(null, "Contraseña Invalida  o Usuario invalido", "Operacion Fallida", JOptionPane.ERROR_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Usuario invalido", "Operacion Fallida", JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

